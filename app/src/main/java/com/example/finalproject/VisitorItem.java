package com.example.finalproject;

public class VisitorItem {
    private String mLine1;
    private String mLine2;

    public VisitorItem(String line1, String line2) {
        this.mLine1 = line1;
        this.mLine2 = line2;
    }

    public String getLine1() {
        return mLine1;
    }

    public String getLine2() {
        return mLine2;
    }
}
